package com.lee.pattern.behavioral.interpreter;

/**
 * @author lihaiqiang
 * @since 2020/9/5
 */
public class TerminalExpression implements Expression {

    private String data;

    public TerminalExpression(String data) {
        this.data = data;
    }

    @Override
    public boolean interpret(String context) {
        if (context.contains(data)) {
            return true;
        }
        return false;
    }
}
